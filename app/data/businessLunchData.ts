// Функция для получения текущего дня недели
export function getCurrentDay() {
    const days = ['sunday', 'monday', 'tuesday', 'wednesday', 'thursday', 'friday', 'saturday'];
    return days[new Date().getDay()];
}

// Функция для получения меню на текущий день
export function getTodayMenu() {
    const today = getCurrentDay();
    return businessLunchData.menu_by_day[today] || [];
}

// Функция для получения блюд по типу курса
export function getDishesByType(dishes: any[], type: string) {
    return dishes.filter(dish => dish.type === type);
}
